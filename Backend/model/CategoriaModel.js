class CategoriaModel{
   constructor(id, nombre){
        this.id = id;
        this.nombre = nombre;
    }
    getid(){
        return this.id;
    }
    setid(id){
        this.id = id;
    }
    getnombre(){
        return this.nombre;
    }
    setnombre(nombre){
        this.nombre = nombre;
   }
}
export default CategoriaModel;

